/**
 * Средняя
 *
 * Гипотеза Коллатца. Рекуррентная последовательность чисел задана следующим образом:
 *
 *   ЕСЛИ (X четное)
 *     Xслед = X /2
 *   ИНАЧЕ
 *     Xслед = 3 * X + 1
 *
 * например
 *   15 46 23 70 35 106 53 160 80 40 20 10 5 16 8 4 2 1 4 2 1 4 2 1 ...
 * Данная последовательность рано или поздно встречает X == 1.
 * Написать функцию, которая находит, сколько шагов требуется для
 * этого для какого-либо начального X > 0.
 */
fun collatzSteps(x: Int): Int {
    var n = x
    var count = 0
    while (n != 1) {
        if (n % 2 == 0)
            n /= 2
        else
            n = n * 3 + 1
        count++
    }
    return count
}

/**
 * Средняя
 *
 * Для заданного x рассчитать с заданной точностью eps
 * sin(x) = x - x^3 / 3! + x^5 / 5! - x^7 / 7! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю.
 * Подумайте, как добиться более быстрой сходимости ряда при больших значениях x.
 * Использовать kotlin.math.sin и другие стандартные реализации функции синуса в этой задаче запрещается.
 */
fun sin(x: Double, eps: Double): Double {
    val ang = x % (2 * PI)
    var sin = ang
    var item = ang
    var factor = -1
    var stage = 3
    var oldItem = item
    while (abs(item) >= eps) {
        item = factor * oldItem * ang.pow(2) / (stage - 1) / stage
        sin += item
        stage += 2
        factor *= -1
        oldItem = abs(item)
    }
    return sin
}

/**
 * Средняя
 *
 * Для заданного x рассчитать с заданной точностью eps
 * cos(x) = 1 - x^2 / 2! + x^4 / 4! - x^6 / 6! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 * Подумайте, как добиться более быстрой сходимости ряда при больших значениях x.
 * Использовать kotlin.math.cos и другие стандартные реализации функции косинуса в этой задаче запрещается.
 */
fun cos(x: Double, eps: Double): Double {
    val ang = x % (2 * PI)
    var cos = 1.0
    var item = 1.0
    var factor = -1
    var stage = 2
    var oldItem = item
    while (abs(item) >= eps) {
        item = factor * oldItem * ang.pow(2) / (stage - 1) / stage
        cos += item
        stage += 2
        factor *= -1
        oldItem = abs(item)
    }
    return cos
}

/**
 * Средняя
 *
 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    var num = n
    var answer = 0
    val len = digitNumber(n)
    var temp = 10.0.pow(len - 1).toInt()
    while (temp > 0) {
        answer += temp * (num % 10)
        temp /= 10
        num /= 10
    }
    return answer
}

/**
 * Средняя
 *
 * Проверить, является ли заданное число n палиндромом:
 * первая цифра равна последней, вторая -- предпоследней и так далее.
 * 15751 -- палиндром, 3653 -- нет.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun isPalindrome(n: Int): Boolean {
    val len = digitNumber(n)
    val a = n % (10.0.pow(len / 2).toInt())
    val b = if (len % 2 == 0)
        n / (10.0.pow(len / 2).toInt())
    else
        n / (10.0.pow(len / 2 + 1).toInt())
    return a == revert(b)
}

/**
 * Средняя
 *
 * Для заданного числа n определить, содержит ли оно различающиеся цифры.
 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun hasDifferentDigits(n: Int): Boolean {
    var num = n
    val len = digitNumber(num)
    val a = num % 10
    for (i in 0 until len) {
        if (a != num % 10)
            return true
        num /= 10
    }
    return false
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var num = 1
    var squared = 0
    var chain = 0
    if (n == 1)
        return 1
    while (chain < n) {
        squared = num.toDouble().pow(2).toInt()
        chain += digitNumber(squared)
        num++
    }
    squared /= 10.0.pow(chain - n).toInt()
    return squared % 10
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из чисел Фибоначчи (см. функцию fib выше):
 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun fibSequenceDigit(n: Int): Int {
    var calcVal = 0
    var chain = 0
    var old = 1
    if (n == 1)
        return 1
    while (chain < n) {
        val a = calcVal
        calcVal += old
        old = a

        chain += digitNumber(calcVal)
    }
    calcVal /= 10.0.pow(chain - n).toInt()
    return calcVal % 10
}
